package Primitive;

public class ConversaoPrimitivoNumerico {
	public static void main(String[] args) {
		
		double a = 1;
		System.out.println(a);
		
		//cast
		float b =  (float) 1.12381283812831283812838123;
		System.out.println(b);
	
		int c = 4; 
		byte d = (byte) c; 
		
		System.out.println(d);
		
		
	}
	
}
